def test_sse_connection(video_id: str, timeout: int = 30):
    """Test Server-Sent Events connection."""
    print(f"ğŸ”„ Testing SSE connection for {video_id}...")
    try:
        import sseclient  # You might need to install this: pip install sseclient-py
        
        url = f"{BASE_URL}/videos/{video_id}/events"
        response = requests.get(url, stream=True, timeout=timeout)
        client = sseclient.SSEClient(response)
        
        event_count = 0
        for event in client.events():
            if event.data:
                try:
                    data = json.loads(event.data)
                    print(f"ğŸ“¡ SSE Event: {data['type']}")
                    event_count += 1
                    
                    if data['type'] == 'complete':
                        print("âœ… SSE connection test completed")
                        return True
                    elif data['type'] == 'error':
                        print(f"âŒ SSE error: {data['message']}")
                        return False
                    elif event_count > 10:  # Limit events for testing
                        print("âœ… SSE connection test passed (limited)")
                        return True
                        
                except json.JSONDecodeError:
                    print(f"ğŸ“¡ SSE Raw data: {event.data}")
                    
    except ImportError:
        print("âš ï¸  SSE test skipped (sseclient-py not installed)")
        print("   Install with: pip install sseclient-py")
        return True
    except Exception as e:
        print(f"âŒ SSE test failed: {e}")
        return False

